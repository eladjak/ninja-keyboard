/** Get content for a specific lesson */
export function getLessonContent(lessonId: string): LessonContent | undefined {
  return LESSON_CONTENT[lessonId]
}

/** Get a random line from a lesson */
export function getRandomLine(lessonId: string): string | undefined {
  const content = LESSON_CONTENT[lessonId]
  if (!content || content.lines.length === 0) return undefined
  const index = Math.floor(Math.random() * content.lines.length)
  return content.lines[index]
}

/** Get all lines for a lesson, optionally shuffled */
export function getLessonLines(
  lessonId: string,
  shuffle: boolean = false,
): string[] {
  const content = LESSON_CONTENT[lessonId]
  if (!content) return []
  if (!shuffle) return content.lines

  const shuffled = [...content.lines]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled
}
